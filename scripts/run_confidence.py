#!/usr/bin/env python
"""Recalculate overall confidence scores for every player.

Prints:
  - Score distribution (how many 5s, 4s, 3s, 2s, 1s, 0s)
  - Average confidence by pyramid step
  - 20 lowest-confidence players for review

Usage::

    python scripts/run_confidence.py
"""

import logging
import sys

from src.etl.confidence import recalculate_confidence

logging.basicConfig(
    level=logging.INFO,
    format="%(asctime)s  %(levelname)-8s  %(name)s  %(message)s",
)
log = logging.getLogger(__name__)


def _print_distribution(dist: dict[int, int], total: int) -> None:
    """Pretty-print a score-distribution histogram."""
    print("\nв•”в•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•—")
    print("в•‘         CONFIDENCE SCORE DISTRIBUTION        в•‘")
    print("в• в•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•Ј")
    for bucket in range(5, -1, -1):
        count = dist.get(bucket, 0)
        pct = (count / total * 100) if total else 0
        bar = "в–Ҳ" * int(pct / 2)
        label = f"{bucket}.00вҖ“{bucket}.99"
        print(f"в•‘  {label}  в”Ӯ {count:>6,}  ({pct:5.1f}%)  {bar}")
    print(f"в• в•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•Ј")
    print(f"в•‘  TOTAL       в”Ӯ {total:>6,}                      в•‘")
    print(f"в•ҡв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•қ")


def _print_step_averages(avg_by_step: dict[int, float]) -> None:
    """Print average confidence by pyramid step."""
    if not avg_by_step:
        print("\n  (no step data available)")
        return
    print("\nв”Ңв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”җ")
    print("в”Ӯ   AVERAGE CONFIDENCE BY STEP        в”Ӯ")
    print("в”ңв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Ө")
    for step in sorted(avg_by_step):
        avg = avg_by_step[step]
        bar = "в–Ҳ" * int(avg * 4)
        print(f"в”Ӯ  Step {step}  в”Ӯ  {avg:.2f}  {bar}")
    print("в””в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҳ")


def _print_lowest(lowest: list[dict]) -> None:
    """Print the 20 lowest-confidence players."""
    if not lowest:
        print("\n  (no players scored)")
        return
    print("\nв”Ңв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”җ")
    print("в”Ӯ   20 LOWEST-CONFIDENCE PLAYERS (for review)            в”Ӯ")
    print("в”ңв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Ө")
    print("в”Ӯ Score в”Ӯ   ID  в”Ӯ Name                                    в”Ӯ")
    print("в”ңв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”јв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”јв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Ө")
    for entry in lowest:
        score = entry["score"]
        pid = entry["player_id"]
        name = entry["name"][:38]
        print(f"в”Ӯ {score:5.2f} в”Ӯ {pid:>5} в”Ӯ {name:<39} в”Ӯ")
    print("в””в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҳ")


def main() -> None:
    log.info("Starting confidence recalculationвҖҰ")
    result = recalculate_confidence()

    _print_distribution(result["distribution"], result["total_scored"])
    _print_step_averages(result["avg_by_step"])
    _print_lowest(result["lowest_20"])

    print(f"\nDone вҖ” {result['total_scored']:,} players scored.")


if __name__ == "__main__":
    try:
        main()
    except KeyboardInterrupt:
        print("\nAborted.")
        sys.exit(1)
    except Exception:
        log.exception("Confidence scoring failed")
        sys.exit(2)
